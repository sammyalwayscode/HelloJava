public class LoopInJava {
    public static void main(String[] args) {
        // While Loop
        int index = 1;
        while (index <= 10) {
            System.out.println(index);
            index++;
        }

        // For Loop
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

    }
}
